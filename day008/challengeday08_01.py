def greet():#Simple Function without parameters
    print("Hello everybody")
    print("Having fun so far?")
    print("I don't think so, jajajaja")

greet()
def greet(name):#Function with input parameters
    print(f"Hello {name}")
    print("Having fun so far?")
    print(f"I don't think so, {name}. Jajajaja\n")

greet("jose")

greet("marco")
